class Field:

# Konstruktor
    def __init__(self, fields, lastRow, lastCol):
        self.fields = fields
        self.lastRow = lastRow
        self.lastCol = lastCol

        pass

# Funktionen
    def getFields():
        return self.fields

    def setFields(fields):
        self.fields = fields

    def getLastRow():
        return self.lastRow

    def setLastCol():
        return self.lastCol
